int	ft_atoi(char *str)
{
	int flag;
	int nb;

	flag = 1;
	nb = 0;
	while (*str == ' ' || *str == '\t' || *str == '\n' || *str == '\v'
			|| *str == '\f' || *str == '\r')
		str++;
	if (*str == '+')
		if (str++ && *str == '+')
			return (0);
	if (*str == '-')
	{
		flag = -1;
		str++;
	}
	while (*str != '\0' && *str >= '0' && *str <= '9')
	{
		nb *= 10;
		nb += *str - '0';
		str++;
	}
	return (nb * flag);
}
