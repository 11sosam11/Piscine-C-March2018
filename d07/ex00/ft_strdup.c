#include <stdlib.h>

char	*ft_strdup(char *src)
{
	char	*s1;
	int		i;

	i = 0;
	while (src[i] != '\0')
	{
		i++;
	}
	s1 = (char *)malloc(sizeof(*s1) * (i + 1));
	i = 0;
	while (src[i] != '\0')
	{
		s1[i] = src[i];
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
