int	ft_strcmp(char *s1, char *s2)
{
	int num;

	num = 0;
	while (s1[num] == s2[num] && (s1[num] != '\0' || s2[num] != '\0'))
		num++;
	return ((int)s1[num] - (int)s2[num]);
}
